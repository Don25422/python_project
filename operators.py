# Arithmetic operators
x = 40
y = 32

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y) # modulus- returns remainder after division

# Comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x == y) # equal to
print(x >= y)
print(x != y) # not equal to

# Assignment operators
a = 10
print(a)
a += 2
print(a)
a *= 2

# logical operators - and, or, not
print(34 < 50 and 56 > 200)
print(34 < 50 or 56 > 200)
print(not(34 < 50 and 56 > 200))